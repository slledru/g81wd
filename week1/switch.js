"use strict"
let season = 'Summer';
switch (season) {
  case 'Summer':
    console.log("Hot and Sunny");
    break;
  case 'Spring':
    console.log('Warm and Sunny');
    break;
  case 'Fall':
    console.log('Cool');
    break;
  case 'Winter':
    console.log('Snowy');
    break;
  default:
    console.log("Unknown Season");
    break;
}
